    private void btnSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSalvarActionPerformed
        // TODO add your handling code here:
        Categoria categoria = (Categoria)cbCategoria.getSelectedItem();
        Produto produto = new Produto();
        produto.setNome(txtNome.getText());
        produto.setDescricao(txtDescricao.getText());
        produto.setPreco(Double.valueOf(txtPreco.getText()));
        produto.setQuantidade(Double.valueOf(txtQuantidade.getText()));
        produto.setCodCategoria(categoria.getId());
        
        ProdutoDAO dao = new ProdutoDAO(produto);
        
        if(dao.salvar()){
            JOptionPane.showMessageDialog(null, "Produto cadastrado com sucesso!");
            carregaTabela();
        }else{
             JOptionPane.showMessageDialog(null, "Ocorreu um erro ao cadastrar o produto!");
        }
    }//GEN-LAST:event_btnSalvarActionPerformed

