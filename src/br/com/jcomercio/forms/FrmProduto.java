    private void carregaTabela(){
        try{
            Connection con = Conexao.getConexao();
            List<Produto> produtos = ProdutoDAO.listar();
            String[] colunas = {"Id", "Nome", "Descrição", "Quantidade", "Preço", "Categoria"};
            Object[][] registros = { ,
            };
            tabela.removeAll();
            tabela.setModel(new DefaultTableModel(registros,colunas){});
            DefaultTableModel dtm = (DefaultTableModel) tabela.getModel();
            
            for(Produto prod : produtos){
                dtm.addRow(new String[]{
                    String.valueOf(prod.getId()),
                    prod.getNome(),
                    prod.getDescricao(),
                    String.valueOf(prod.getQuantidade()),
                    String.valueOf(prod.getPreco()),
                    prod.getCategoriaNome()
            });
            }
        }catch(SQLException ex){
            JOptionPane.showMessageDialog(null,"Erro ao carregar os dados da tabela.\nMensagem do Servidor: "+ ex.getMessage());
        }
    }

