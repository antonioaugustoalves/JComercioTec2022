    private void carregarTabela(){
        try{
            Connection con = Conexao.getConexao();
            List<Categoria> categorias = CategoriaDAO.listar();
            String[] colunas = {"Id", "Categoria", "Descrição"};
            Object[][] registros = { , };
            tabela.removeAll();
            tabela.setModel(new DefaultTableModel(registros, colunas){});
            DefaultTableModel dtm = (DefaultTableModel)tabela.getModel();
            
            for(Categoria cat: categorias){
                dtm.addRow(new String[]{
                    String.valueOf(cat.getId()),
                    cat.getNome(),
                    cat.getDescricao()
                });
            }
            
            System.out.println("Carregando os dados da tabela na tela.");
            
        }catch(SQLException ex){
            JOptionPane.showMessageDialog(null, 
                    "Erro ao carregar a tabela.\n"
                    +"Mensagem do servidor: "+ ex.getMessage()
                    +"Causa do erro: "+ ex.getCause());
        }
    } 
