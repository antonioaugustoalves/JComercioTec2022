    private void btnPesquisarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnPesquisarActionPerformed
       int idProcurado = Integer.valueOf(txtId.getText());
       Categoria categoria = new Categoria();
       categoria.setId(idProcurado);
       CategoriaDAO dao = new CategoriaDAO(categoria);
       
       if(dao.buscarPorId()){
           txtNome.setText(categoria.getNome());
           txtDescricao.setText(categoria.getDescricao());
       }else{
           JOptionPane.showMessageDialog(null,
                   "A busca não retornou resultados.");
       }
    }//GEN-LAST:event_btnPesquisarActionPerformed

    private void btnSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSalvarActionPerformed
        // TODO add your handling code here:
        Categoria cat = new Categoria();
        String nome = txtNome.getText();
        String descricao = txtDescricao.getText();
        cat.setNome(nome);
    
        cat.setDescricao(descricao);
        CategoriaDAO dao = new CategoriaDAO(cat);
        
        if(dao.salvar()){
            JOptionPane.showMessageDialog(null,
                    "Categoria salva com sucesso.");
            carregarTabela();
        }else{
           JOptionPane.showMessageDialog(null,
                    "Erro ao salvar a categoria."); 
        }
        
    }//GEN-LAST:event_btnSalvarActionPerformed

    private void btnAtualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAtualizarActionPerformed
        int id = Integer.valueOf(txtId.getText());
        Categoria categoria = new Categoria();
        categoria.setId(id);
        categoria.setNome(txtNome.getText());
        categoria.setDescricao(txtDescricao.getText());
        CategoriaDAO dao = new CategoriaDAO(categoria);
        
        if(dao.atualizar()){
       
            JOptionPane.showMessageDialog(null,
                    "Registro atualizado com sucesso!");
            carregarTabela();
        }else{
           JOptionPane.showMessageDialog(null,
                    "Não foi possivel atualizar o registro!"); 
        }

    }//GEN-LAST:event_btnAtualizarActionPerformed

